package com.jobspot.master;

public class Town {
	
	private String code;
	private String name;
	
	public Town(){
		
	}
	
	public void setCode(String code){
		this.code = code;
	}

	public String getCode(){
		return this.code;
	}
	
	public void setName(String name){
		this.name = name;
	}

	public String getName(){
		return this.name;
	}
}
